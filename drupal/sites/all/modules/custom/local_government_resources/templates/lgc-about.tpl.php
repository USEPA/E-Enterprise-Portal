      <div class="field-item even" property="content:encoded"><p>The Resources for Local Communities widget gives you
          state, tribal, local, and federal resources that are recommended by environmental specialists. These include
          tools, calculators, guides, and other relevant material.</p>
        <p>The <b>All</b> tab includes all available resources. The <b>My Resources</b> tab displays just those that
          match the Topics you picked at the Getting Started screen or <b>Manage My Profile Topics</b>. The <b>Manage My
            Profile Topics</b> lets you see what interests you've selected in your Profile and customize them.</p>
        <p>You can use the <b>Filter</b> box to find resources with matching keywords or phrases.</p>
        <p>The <b>Adjust Selections</b> sidebar on the left side lets you narrow down the list and includes resources
          that match the choices you select. You can click Adjust Selections &lt; to collapse the sidebar and see more
          details for each resource and use the information <img
            src="sites/all/modules/custom/recommended_resources/images/information.png"> icon to open a details dialog
          for each as well.</p>
        <ul>
          <li><b>Source</b> – This is where the information located and where you go if you click the link.</li>
          <li><b>Topic</b> – This is the main environmental issue you may want information on. Each of the tools or
            resources is classified under a topic to allow you to filter the most important information.
          </li>
          <li><b>Category</b> – These are classification that each of the resources fall under to help sort your
            options.
          </li>
          <li><b>Tool Type</b> – This will show you what type of resource you will interact with before visiting the
            provided website link. Examples of tool are:
            <ul>
              <li>Website</li>
              <li>Decision Tree</li>
              <li>Guidance</li>
            </ul>
          </li>
          <li><b>Training Level</b> - The amount of training you may need in order to use a specific resource. A high
            training level typically means you will need a vast amount of knowledge in the specific resource or field to
            use that resource. A low training level typically means you will be able to use a resource with minimal
            knowledge.
          </li>
          <li><b>Data Requirements</b> - Some resources require higher bandwidth or higher powered computers depending
            on the application or website. A “High” resource requirement will require more bandwidth or computing power
            to access. In some instances it may mean costs as well.
          </li>
          <li><b>Relevance</b> – This column provides information on the type of user or person that may find the
            particular resource helpful.
          </li>
        </ul>
      </div>
