<p>
  Have a well and wonder what your water testing results mean?
</p>
<p>
  The Be Well Informed Guide lets you enter your test results and get feedback about health concerns and water treatment
  choices.
</p>
<p>
  Be Well Informed was created for New Hampshire residents, and has the <a
    href="http://des.nh.gov/organization/divisions/water/dwgb/well_testing/documents/well-testing.pdf" target="_blank">most
    common contaminants</a> that affect wells
  there. It may not have all the contaminants present in your area - for those it does have, it can provide useful
  information.
</p>
<p>
  A quick disclaimer before we start.
</p>
<p class="widget-note">
  Information provided on this website is for informational purposes only and should not be substituted for direct
  consultation with a qualified water treatment professional. Other conditions or factors related to your well or home
  not considered by this online guide may determine the most appropriate water treatment option.
</p>
